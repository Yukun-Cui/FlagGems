def unbind_kernel(
    input_ptr,
    output_ptr,
    dim_size,
    dim_prod_post,
    num_elements,
    BLOCK_SIZE: tl.constexpr,
):
    """Kernel for unbind.

    Uses a 2D grid: (num_blocks, dim_size)
    - program_id(0): block index within one output tensor
    - program_id(1): which output tensor (slice) to copy
    """
